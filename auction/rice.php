	<center><h2 class="c">Importance Of Rice</h2></center>	
	Rice, a staple food for more than half of the world's population, is grown in >100 countries with 90% of the total global production from Asia. Although there are more than 110,000 cultivated varieties of rice that vary in quality and nutritional content, after post-harvest processing, rice can be categorized as either white or brown. Regional and cultural preferences as well as need for stability during storage and transport are the final determinants of market availability and final consumption. In addition to calories, rice is a good source of magnesium, phosphorus, manganese, selenium, iron, folic acid, thiamin and niacin; but it is low in fiber and fat. Although brown rice is promoted as being "healthier" because of bioactive compounds, including minerals and vitamins not present in white rice after polishing, white rice is more widely consumed than brown. This is for several reasons, including cooking ease, palatability, and shelf life. Polished rice has a higher glycemic load and may impact glucose homeostasis but when combined with other foods, it can be considered part of a "healthy" plate. With the projected increase in the global population, rice will remain a staple. However, it will be important to encourage intake of the whole grain (brown rice) and to identify ways to harness the phytonutrients that are lost during milling. 
