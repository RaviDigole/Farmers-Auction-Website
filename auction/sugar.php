	<h2 class="c">What Is Sugercane?</h2>
    Sugarcane or sugar cane is a species of (often hybrid) tall, perennial grass (in the genus Saccharum, tribe Andropogoneae) that is used for sugar production. The plants are 2–6 m (6–20 ft) tall with stout, jointed, fibrous stalks that are rich in sucrose,[1] which accumulates in the stalk internodes. Sugarcanes belong to the grass family, Poaceae, an economically important flowering plant family that includes maize, wheat, rice, and sorghum, and many forage crops. It is native to the warm temperate and tropical regions of India, Southeast Asia, and New Guinea. Grown in tropical and subtropical regions, sugarcane is the world's largest crop by production quantity, totaling 1.9 billion tonnes in 2020, with Brazil accounting for 40% of the world total. Sugarcane accounts for 79% of sugar produced globally (most of the rest is made from sugar beets). About 70% of the sugar produced comes from Saccharum officinarum and its hybrids.All sugarcane species can interbreed, and the major commercial cultivars are complex hybrids.
