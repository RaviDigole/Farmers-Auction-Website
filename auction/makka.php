	Corn is an essential crop that plays a significant role in our daily lives. It is the most widely distributed food crop globally and is used for a variety of purposes, including animal feed, grain for human consumption, ethanol, high fructose corn syrup, sweeteners, starch, and beverage/alcohol production1234. The United States is the largest producer of corn, and it is by no means the only one. China, the next largest producer, plants nearly the same amount of acreage as the US, although it yields about twenty-five to thirty percent less. In third place is Brazil, which produces less than half of China’s output1.Corn is a crucial component of the US economy, contributing to 10% of the fuel for all cars in the US. Ethanol accounts for more than forty percent of all the corn produced, and much of the remaining sixty percent goes to livestock feed, fueling the animals that feed the citizens1. Corn is also used in an ever-growing and mind-boggling array of applications. More than two thousand grocery items contain corn in one form or another, and corn touches our lives in multiple ways on a daily basis. For instance, the carpet beneath your feet may be applied using an adhesive containing corn, and it might even be made, in part, from that same remarkable plant1.
    
