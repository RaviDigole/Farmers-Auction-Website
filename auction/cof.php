	<h2 class="c">What Is Tea?</h2>
   Originating in India nearly 5,000 years ago, chai has been loved by many. Legend has it that chai didn’t originally contain tea leaves, milk or sugar when it was first created. It was only comprised of a mix of spices native to India and was used for Ayurveda, an all-natural Indian healing practice. (Fun fact: “chai” means tea, so when you say “chai tea,” you’re really saying “tea tea.”Flash-forward to the 1600s when black tea leaves were discovered in China. Once the tea leaf, known as Camellia sinensis assamica, made its way to India, the chai recipe we know and love today was created.Today, chai tea is a brewed black tea prepared with multiple aromatic spices like ginger and cardamom, milk and sugar. This blend gives chai its full-bodied, spice-filled flavor and creamy mouthfeel.
